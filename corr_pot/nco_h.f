
      real*8 function nco_h(r, rpar, ipar)
c
c     sample main program for calling h+nco potential
c
c     coordinates:
c                          H
c                           \
c                        N---C---O
c
c     Input coordinates:
c           r     = R(C-H)             4.0 <   r   < 15.0 au
c           theta = H-C-N Angle     -180.0 < theta < +180. degrees
c
c
c     Fixed coordinates:    R(CN) = 2.37 au
c                           R(CO) = 2.24 au
c                           N-C-N = 180 degrees
c
c     Ouput energy in au relative to H+NCN
c


      implicit double precision (a-h,o-z)

      parameter (nfrag=2, natommx=100, ndim = 3)

      dimension r(nfrag,natommx,ndim)
      dimension rch(ndim),rnc(ndim),rnh(ndim)

      include 'data.fi'

      pi2 = asin(1.0d0)
      pi = 2.0d0*pi2

c
c     for nco + h -- just need to calculate r and theta 
c

      rch2 = 0.0d0
      rnc2 = 0.0d0
      rnh2 = 0.0d0
 
      do 1100 idim = 1 , ndim
 
         rch(idim) = r(1,1,idim) - r(2,1,idim)
         rnc(idim) = r(1,2,idim) - r(1,1,idim)
         rnh(idim) = r(2,1,idim) - r(1,2,idim)
         rch2 = rch2 + rch(idim)**2
         rnc2 = rnc2 + rnc(idim)**2
         rnh2 = rnh2 + rnh(idim)**2
 
1100  continue

      rch0 = sqrt(rch2)
      rnc0 = sqrt(rnc2)
      rnh0 = sqrt(rnh2)
 
      cthe = (rch2 + rnc2 - rnh2)/(2.0d0*rch0*rnc0)
      if (abs(cthe).gt.1.0d0) then
         write (6,*) 'error in cthe',cthe
         if (cthe.gt.1.0d0) then
            the = 0.0d0
         else
            the = pi
         endif
      else
         the = acos(cthe)
      endif

      theta = 180.0d0*the/pi
      if (rch0.lt.4.0) rch0=4.0
      if (rch0.gt.15.) rch0=15.
      call hPnco_atz_dci(rch0,theta,nco_h)

      return
      end


